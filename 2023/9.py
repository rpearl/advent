lines = data.splitlines()

ints = u.ints(data)
intlines = u.lmap(u.ints, lines)
toklines = [line.split(' ') for line in lines]

def predict(vals, first):
    if all(v == 0 for v in vals):
        return 0
    diffs = [vals[i] - vals[i-1] for i in range(1,len(vals))]
    p = predict(diffs, first)
    if first:
        return vals[0] - p
    else:
        return vals[-1] + p


def a():
    return sum(predict(line, first=False) for line in intlines)
    pass


def b():
    return sum(predict(line, first=True) for line in intlines)
    pass

def main():
    submit = globals().get('submit', False)
    astart = time.perf_counter()
    ra = a()
    aend = time.perf_counter()
    if ra is not None:
        print(f"Part a: {ra}")
        print(f"Time taken: {aend-astart:.4f} sec")
        if submit:
            sbmt(ra, part="a")

    bstart = time.perf_counter()
    rb = b()
    bend = time.perf_counter()
    if rb is not None:
        print(f"Part b: {rb}")
        print(f"Time taken: {bend-bstart:.4f} sec")
        if submit:
            sbmt(rb, part="b")
    print()
main()
